# 파이썬 자료형

# 숫자 자료형
print(1004)
print(3.14)

# 문자 자료형
print('Hello World!')
print('재미있는 파이썬')

# 불리안 자료형 
print(True)
print(False)

# 출력함수 print()

# 주석, 내 이름으로 삼행시 만들기
print('김 : 김상아')
print('상 : 상아')
print('아 : 아아아아')

# 여러 자료를 출력 콤마(,)
print('황 : 황홀한 코드의 세계에 입문한 당신,',
      '미 : 미소 짓게 만드는 첫 출력 함수와 함께',
      '옥 : 옥처럼 빛나는 파이썬 실력을 쌓아가요!')



# 숫자형 자료 출력
print(100)
print(100 + 200)
print('100 + 200', ' = ', 100+200)

# 숫자의 자료형 확인하기
num1 = 10
num2 = 3.14
print(type(num1))
print(type(num2))

# 숫자와 문자 출력하기
print(3 + 4)
print('3 + 4')
print('3 + 4 = ', 3+4)

print(bool(None))
